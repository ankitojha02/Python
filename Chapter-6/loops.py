# While loop
b = 1
while b < 5:
    print(b)
    b += 1

#for loop
# range(start, stop, step)
for i in range(1, 10, 2):
    print(i)

# list using while loop
list = ["apple", "banana", "cherry"]

i = 0
while i < len(list):
    print(list[i])
    i += 1
