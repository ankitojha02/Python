a = 1
b = 2
print(a + b)

#String variable
name = "Ankit"
print("My name is " + name)

#Boolean variable
is_student = True
print("Is Ankit a student? " + str(is_student))

#None variable
nothing = None
print("The value of nothing is: " + str(nothing))

# Variable naming rules:
# 1. Variable names can contain letters, numbers, and underscores.
# 2. Variable names must start with a letter or an underscore.
# 3. Variable names are case-sensitive.
# 4. Variable names cannot be a reserved keyword in Python.
# 5. No whitespace is allowed in variable names. Use underscores instead.
