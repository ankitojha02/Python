# Assignment Operators
x = 5
x += 3  # Equivalent to x = x + 3
print(x)  # Output: 8

# Logical Operators
a = True
b = False
print(a and b)  # Output: False
print(a or b)   # Output: True

# Comparison Operators
num1 = 10
num2 = 20
print(num1 == num2)  # Output: False
print(num1 != num2)  # Output: True
print(num1 < num2)   # Output: True
print(num1 > num2)   # Output: False
print(num1 >= num2)  # Output: False
print(num1 <= num2)  # Output: True


